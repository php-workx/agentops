#!/bin/bash
# Local plugin validation - run before pushing
# Usage: ./scripts/validate-local.sh [plugin-name]
#
# Validates:
# 1. Manifest structure (no invalid keys)
# 2. No symlinks (breaks GitHub install)
# 3. SKILL.md frontmatter
# 4. Actually loads with claude --plugin-dir
#
# Install as pre-push hook:
#   ln -sf ../../scripts/validate-local.sh .git/hooks/pre-push

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
NC='\033[0m'

pass() { echo -e "${GREEN}✓${NC} $1"; }
fail() { echo -e "${RED}✗${NC} $1"; errors=$((errors + 1)); }
warn() { echo -e "${YELLOW}!${NC} $1"; }

errors=0
cd "$REPO_ROOT"

echo "═══════════════════════════════════════════════════════"
echo "  AgentOps Local Plugin Validation"
echo "═══════════════════════════════════════════════════════"
echo ""

# Determine which plugins to validate
if [[ $# -gt 0 ]]; then
    plugins=("$@")
else
    plugins=()
    for p in plugins/*/; do
        plugins+=("$(basename "$p")")
    done
    # Also check root plugin
    plugins+=("root")
fi

# Valid manifest keys
valid_keys='["name","version","description","author","homepage","repository","license","keywords","commands","skills","agents"]'

for plugin in "${plugins[@]}"; do
    if [[ "$plugin" == "root" ]]; then
        plugin_dir="."
        manifest=".claude-plugin/plugin.json"
    else
        plugin_dir="plugins/$plugin"
        manifest="$plugin_dir/.claude-plugin/plugin.json"
    fi

    echo "── $plugin ──"

    # Skip if doesn't exist
    if [[ ! -d "$plugin_dir" ]] && [[ "$plugin" != "root" ]]; then
        fail "Directory not found: $plugin_dir"
        continue
    fi

    # 1. Check manifest exists and is valid JSON
    if [[ ! -f "$manifest" ]]; then
        fail "Missing manifest: $manifest"
        continue
    fi

    if ! jq empty "$manifest" 2>/dev/null; then
        fail "Invalid JSON in manifest"
        continue
    fi

    # 2. Check for invalid keys
    invalid=$(jq -r --argjson valid "$valid_keys" 'keys - $valid | .[]' "$manifest" 2>/dev/null)
    if [[ -n "$invalid" ]]; then
        fail "Invalid manifest keys: $invalid"
    else
        pass "Manifest valid"
    fi

    # 3. Check for symlinks
    if [[ "$plugin" != "root" ]]; then
        symlinks=$(find "$plugin_dir" -type l 2>/dev/null | wc -l | tr -d ' ')
        if [[ $symlinks -gt 0 ]]; then
            fail "Contains $symlinks symlinks (breaks standalone install):"
            find "$plugin_dir" -type l 2>/dev/null | sed 's/^/    /'
        else
            pass "No symlinks"
        fi
    fi

    # 4. Check skills
    if [[ "$plugin" != "root" ]] && [[ -d "$plugin_dir/skills" ]]; then
        skill_count=0
        skill_errors=0
        for skill_dir in "$plugin_dir/skills"/*/; do
            [[ ! -d "$skill_dir" ]] && continue
            skill_name=$(basename "$skill_dir")
            skill_file="$skill_dir/SKILL.md"

            if [[ ! -f "$skill_file" ]]; then
                fail "Skill $skill_name: missing SKILL.md"
                skill_errors=$((skill_errors + 1))
                continue
            fi

            # Check frontmatter
            if ! head -1 "$skill_file" | grep -q "^---$"; then
                fail "Skill $skill_name: no YAML frontmatter"
                skill_errors=$((skill_errors + 1))
                continue
            fi

            if ! grep -q "^name:" "$skill_file"; then
                fail "Skill $skill_name: missing 'name' in frontmatter"
                skill_errors=$((skill_errors + 1))
                continue
            fi

            skill_count=$((skill_count + 1))
        done

        if [[ $skill_errors -eq 0 ]] && [[ $skill_count -gt 0 ]]; then
            pass "$skill_count skills valid"
        fi
    fi

    # 5. Test actual load with Claude CLI (if available)
    if command -v claude &>/dev/null && [[ "$plugin" != "root" ]]; then
        load_output=$(timeout 10 claude --plugin-dir "$plugin_dir" --help 2>&1) || true
        if echo "$load_output" | grep -qiE "invalid manifest|validation error|failed to load"; then
            fail "Claude CLI load failed"
            echo "$load_output" | grep -iE "invalid|failed|error" | head -3 | sed 's/^/    /'
        else
            pass "Claude CLI loads plugin"
        fi
    fi

    echo ""
done

# Summary
echo "═══════════════════════════════════════════════════════"
if [[ $errors -gt 0 ]]; then
    echo -e "${RED}  VALIDATION FAILED: $errors errors${NC}"
    echo "═══════════════════════════════════════════════════════"
    exit 1
else
    echo -e "${GREEN}  ALL VALIDATIONS PASSED${NC}"
    echo "═══════════════════════════════════════════════════════"
    exit 0
fi
